const px = (rows: string[]): string[] => {
  const width = Math.max(...rows.map((row) => row.length));
  return rows.map((row) =>
    row
      .padEnd(width, ".")
      .replace(/#/g, "1")
      .replace(/[.\s]/g, "0")
  );
};

