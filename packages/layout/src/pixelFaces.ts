export function selectPixelFace(faceType: PixelFaceType, seed = ""): PixelFace {
  const faces = pixelFaceLibrary[faceType] ?? pixelFaceLibrary.awkward_speechless;
  if (!seed) return faces[Math.floor(Math.random() * faces.length)] ?? faces[0];
  const index = hash(seed || faceType) % faces.length;
  return faces[index] ?? faces[0];
}

function hash(value: string): number {
  return [...value].reduce((sum, char) => (sum * 31 + char.charCodeAt(0)) >>> 0, 7);
}

export const pixelFaces = Object.fromEntries(
  Object.entries(pixelFaceLibrary).map(([category, faces]) => [category, faces[0]?.matrix ?? []])
) as Record<PixelFaceType, string[]>;
